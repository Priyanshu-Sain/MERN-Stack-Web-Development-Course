/*
Comparator operators are of two types based on what they compares between the two things.
  1) ==
    This is loose equality operator and it only compares the value and converts the type
    automatically.
    i.e. (1 == '1') will give True.
  2) ===
    This is strict equality operator and it compares both type and value of the two quantities
    i.e. (1 === '1') will five False.

NOTE : By opening the terminal and writing node to it will open a REPL 
       which stands for Read , Evaluate , Print and Loop
       To close REPL , press Ctrl+D
*/